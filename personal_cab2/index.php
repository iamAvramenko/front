            if ($(this).hasClass('open')) {
                $("#menu, #fade").removeClass('active');
                $(this).removeClass('open');
            } else {
                $("#menu, #fade").addClass('active');
                $(this).addClass('open');
            }
        });

        $("#fade").on("click", function () {
            $("#menu, #fade, .popup").removeClass('active');
            $(".open_menu").removeClass('open');
        });

        /* POPUPS */

        $(".popup .close").on("click", function () {
            $(".popup").removeClass("active");
        });

        $("[data-popup]").on("click", function () {
            $(".popup").removeClass('active');
            var popup = $(this).data("popup");
            $("#" + popup).addClass('active');
        });

        $("[data-step]").on("click", function () {
            var step = $(this).data('step');
            var popup = $(this).closest('.popup');
            popup.find('.step').fadeOut();
            popup.find('.step.step' + step).fadeIn().css({'display': 'flex'});
        });

